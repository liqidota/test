package com.liqi.bdqn.eneity;

public class MyUser {
		private String myname;
		private String mypwd;
		public MyUser(String myname, String mypwd) {
			this.myname = myname;
			this.mypwd = mypwd;
		}
		public String getMyname() {
			return myname;
		}
		public void setMyname(String myname) {
			this.myname = myname;
		}
		public String getMypwd() {
			return mypwd;
		}
		public void setMypwd(String mypwd) {
			this.mypwd = mypwd;
		}
		public MyUser() {
			
		}
		
}
